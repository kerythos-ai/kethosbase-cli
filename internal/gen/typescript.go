// Package gen renders introspected schemas into source code. Today: TypeScript.
package gen

import (
	"fmt"
	"regexp"
	"sort"
	"strings"

	"github.com/kerythos-ai/kethosbase-cli/internal/introspect"
)

var identRE = regexp.MustCompile(`^[A-Za-z_][A-Za-z0-9_]*$`)

// TypeScript renders a Supabase-shaped `Database` type: per-table Row/Insert/
// Update plus an Enums map, with each enum also exported as a named union.
func TypeScript(s *introspect.Schema) string {
	enumAlias := map[string]string{} // pg enum typename -> TS alias
	for _, e := range s.Enums {
		enumAlias[e.Name] = pascal(e.Name)
	}

	var b strings.Builder
	b.WriteString("// Code generated by `kethosbase gen types`. DO NOT EDIT.\n\n")
	b.WriteString("export type Json =\n  | string\n  | number\n  | boolean\n  | null\n  | { [key: string]: Json | undefined }\n  | Json[];\n\n")

	// Named enum unions (stable order).
	enums := append([]introspect.Enum(nil), s.Enums...)
	sort.Slice(enums, func(i, j int) bool { return enums[i].Name < enums[j].Name })
	for _, e := range enums {
		b.WriteString(fmt.Sprintf("export type %s = %s;\n", pascal(e.Name), unionOf(e.Labels)))
	}
	if len(enums) > 0 {
		b.WriteString("\n")
	}

	b.WriteString("export interface Database {\n")
	b.WriteString(fmt.Sprintf("  %s: {\n", key(s.Name)))

	// Tables.
	b.WriteString("    Tables: {\n")
	for _, t := range s.Tables {
		b.WriteString(fmt.Sprintf("      %s: {\n", key(t.Name)))
		b.WriteString("        Row: {\n")
		for _, c := range t.Columns {
			b.WriteString(fmt.Sprintf("          %s: %s;\n", key(c.Name), rowType(c, enumAlias)))
		}
		b.WriteString("        };\n")
		b.WriteString("        Insert: {\n")
		for _, c := range t.Columns {
			b.WriteString(fmt.Sprintf("          %s%s: %s;\n", key(c.Name), insertOpt(c), rowType(c, enumAlias)))
		}
		b.WriteString("        };\n")
		b.WriteString("        Update: {\n")
		for _, c := range t.Columns {
			b.WriteString(fmt.Sprintf("          %s?: %s;\n", key(c.Name), rowType(c, enumAlias)))
		}
		b.WriteString("        };\n")
		b.WriteString("      };\n")
	}
	b.WriteString("    };\n")

	// Enums map.
	b.WriteString("    Enums: {\n")
	for _, e := range enums {
		b.WriteString(fmt.Sprintf("      %s: %s;\n", key(e.Name), pascal(e.Name)))
	}
	b.WriteString("    };\n")

	b.WriteString("  };\n}\n")
	return b.String()
}

// rowType is the column's TS type including a `| null` for nullable columns.
func rowType(c introspect.Column, enums map[string]string) string {
	t := scalarOrArray(c.UDTName, enums)
	if c.Nullable {
		return t + " | null"
	}
	return t
}

// insertOpt makes a column optional on Insert when the database can supply it:
// it has a default, is an identity column, or is nullable.
func insertOpt(c introspect.Column) string {
	if c.HasDefault || c.IsIdentity || c.Nullable {
		return "?"
	}
	return ""
}

func scalarOrArray(udt string, enums map[string]string) string {
	elem, isArray := udt, false
	if strings.HasPrefix(udt, "_") {
		elem, isArray = udt[1:], true
	}
	base, ok := enums[elem]
	if !ok {
		base = scalar(elem)
	}
	if isArray {
		return base + "[]"
	}
	return base
}

// scalar maps a Postgres udt_name to a TypeScript type.
func scalar(udt string) string {
	switch udt {
	case "bool":
		return "boolean"
	case "int2", "int4", "int8", "float4", "float8", "numeric", "money", "oid":
		return "number"
	case "json", "jsonb":
		return "Json"
	default:
		// text, varchar, bpchar, uuid, timestamptz, date, time, bytea, … → string.
		return "string"
	}
}

func unionOf(labels []string) string {
	parts := make([]string, len(labels))
	for i, l := range labels {
		parts[i] = `"` + l + `"`
	}
	return strings.Join(parts, " | ")
}

// key quotes an object key only when it is not a plain identifier.
func key(name string) string {
	if identRE.MatchString(name) {
		return name
	}
	return `"` + name + `"`
}

// pascal turns snake_case into PascalCase for enum type aliases.
func pascal(s string) string {
	var b strings.Builder
	for _, part := range strings.Split(s, "_") {
		if part == "" {
			continue
		}
		b.WriteString(strings.ToUpper(part[:1]))
		b.WriteString(part[1:])
	}
	if b.Len() == 0 {
		return "Enum"
	}
	return b.String()
}
